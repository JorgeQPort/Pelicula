				<section class="hist">
						<p>La historia del cine como espectáculo comenzó en París el 28 de diciembre de 1895.
						Desde entonces ha experimentado una serie de cambios en varios sentidos. Por un lado, la tecnología del cinematógrafo ha evolucionado mucho, desde sus inicios con el cine mudo de los hermanos Lumière hasta el cine digital del siglo XXI. Por otro lado, ha evolucionado el lenguaje cinematográfico, incluidas las convenciones del género, y han surgido así distintos géneros cinematográficos. En tercer lugar, ha evolucionado con la sociedad, con lo que se desarrollaron distintos movimientos cinematográficos.</p>
						<h2> Causas que dieron lugar al nacimiento del cine</h2>
						<p>La aparición de nuevas cargas de energía mineral y la aplicación de la electricidad a usos industriales.
						La erradicación de las epidemias y el desarrollo de la medicina.
						Los nuevos medios de transporte (buque a vapor, ferrocarril), así como las innovaciones orientadas a la comunicación (telégrafo, cable submarino, telegrafía sin hilos), trajeron consigo una amplia y rápida difusión de la información, lo que condujo a una globalización efectiva.
						La consolidación de las ciudades: aparición de un público de masas. </p>
						<h2>Inicio del Cine</h2>
						<p>La idea de capturar, crear y reproducir el movimiento por medios mecánicos es muy antigua, existieron antecedentes tales como la cámara oscura, o el taumatropo, la linterna mágica, el fusil fotográfico. La técnica para captar la realidad por medios luminosos había sido ya desarrollada por los inventores del daguerrotipo y la fotografía, a mediados del siglo XIX.</p>
						<h2>Distribución de las películas</h2>
						<p>Siendo el cine el único multimedia de la época, y estando su producción bien controlada por los grandes estudios, se desarrolló todo un mecanismo de producción industrial de películas. Así, se programaba no la exhibición de una película pura, sino de verdaderos rotativos que ofrecían, por el mismo precio, una serial y dos películas. A la película principal se sumaba una de menor costo, y producción barata y apresurada, que por su condición de relleno, pasó a ser llamada serie B. Posteriormente la serie B evolucionaría hasta ser un género por derecho propio, pero en aquel tiempo, iba adosada al cine normal.</p>
